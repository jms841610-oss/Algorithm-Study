#include <iostream>
#include <vector>

using namespace std;

int main(){
    // C++ 입출력 속도 향상을 위한 설정입니다. (알고리즘 문제 풀이 시 필수적인 부분이죠!)
    ios::sync_with_stdio(false);
    cin.tie(NULL);

    int N;
    cin >> N; // 입력받을 상황(투구 결과 등)의 총 개수

    int cnt_ball = 0; // 누적되는 볼 카운트를 저장하는 변수

    // 크기가 4이고 모두 0으로 초기화된 벡터를 생성합니다.
    // 인덱스의 의미: area[0] = 홈(득점), area[1] = 1루, area[2] = 2루, area[3] = 3루
    vector<int> area(4,0);

    for(int i = 0; i < N; i++){
        int type;
        cin >> type; // 이번 턴의 결과 (예: 1=볼, 2=안타, 3=특정 진루 상황)

        // type이 1이거나 3일 경우 볼 카운트를 1 증가시킵니다.
        if((type == 1) || (type == 3)) cnt_ball++;

        // 포볼(볼 4개 누적)이거나, type 2(안타), type 3 상황일 때 진루 및 득점 로직을 실행합니다.
        if((cnt_ball == 4) || (type == 2) || (type == 3)){
            
            // [3루 -> 홈 진루 (득점)]
            // 1) 만루(1, 2, 3루 모두 주자 존재) 상황이라 밀어내기가 발생하거나
            // 2) type 3 상황이면서 3루에 주자가 있을 때
            if(((area[1] != 0) && (area[2] != 0) && (area[3] != 0)) || ((type == 3) && (area[3] != 0))){
                area[0] += area[3]; // 3루 주자가 홈으로 들어와 득점(area[0])에 합산
                area[3] = 0;        // 홈으로 들어왔으니 3루는 비워줍니다.
            }
            
            // [2루 -> 3루 진루]
            // 1) 1, 2루에 주자가 있어서 타자 출루 시 2루 주자가 밀려나야 하거나
            // 2) type 3 상황이면서 2루에 주자가 있을 때
            if(((area[1] != 0) && (area[2] != 0)) || ((type == 3) && (area[2] != 0))){
                area[3] += area[2]; // 2루 주자가 3루로 이동
                area[2] = 0;        // 2루는 비워줍니다.
            }
            
            // [1루 -> 2루 진루]
            // 1루에 주자가 있다면, 타자가 1루로 출루해야 하므로 기존 1루 주자는 2루로 밀려납니다.
            if((area[1] != 0)){
                area[2] += area[1]; // 1루 주자가 2루로 이동
                area[1] = 0;        // 1루는 비워줍니다.
            }
            
            // [타자의 1루 출루 및 볼 카운트 초기화]
            // 포볼(cnt_ball == 4)이거나 type 2(안타)인 경우 타자가 1루로 진루합니다.
            if((cnt_ball == 4) || (type == 2)){
                area[1]++;    // 1루에 새로운 주자(타자) 배치
                cnt_ball = 0; // 출루했으므로 볼 카운트는 다시 0으로 초기화
            }
        }
    }
    
    // 최종적으로 누적된 홈(득점)의 값을 출력합니다.
    cout << area[0];
    return 0; 
}
